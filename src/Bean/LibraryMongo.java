package Bean;

public class LibraryMongo 
{
	private String status;
	private String librarySynMasterLog;
	private String createdDate;
	private String Percentage;
	
	public String getStatus() 
	{
		return status;
	}
	
	public void setStatus(String status) 
	{
		this.status = status;
	}
	public String getLibrarySynMasterLog() 
	{
		return librarySynMasterLog;
	}
	public void setLibrarySynMasterLog(String librarySynMasterLog) 
	{
		this.librarySynMasterLog = librarySynMasterLog;
	}
	public String getCreatedDate() 
	{
		return createdDate;
	}
	public void setCreatedDate(String createdDate) 
	{
		this.createdDate = createdDate;
	}
	public String getPercentage() 
	{
		return Percentage;
	}
	public void setPercentage(String percentage) 
	{
		Percentage = percentage;
	}
}
