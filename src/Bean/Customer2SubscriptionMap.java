package Bean;

public class Customer2SubscriptionMap 
{
	private String customerId;
	private String next_subscription_run_date;
	private String subscribedOn;
	
	public String getCustomerId() 
	{
		return customerId;
	}
	public void setCustomerId(String customerId) 
	{
		this.customerId = customerId;
	}
	public String getNext_subscription_run_date() 
	{
		return next_subscription_run_date;
	}
	public void setNext_subscription_run_date(String next_subscription_run_date) 
	{
		this.next_subscription_run_date = next_subscription_run_date;
	}
	public String getSubscribedOn() 
	{
		return subscribedOn;
	}
	public void setSubscribedOn(String subscribedOn) 
	{
		this.subscribedOn = subscribedOn;
	}
}
